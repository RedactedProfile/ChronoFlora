	@Override
	public void create () {
		batch = new SpriteBatch();

		camera = new OrthographicCamera();
		camera.setToOrtho(true, Gdx.graphics.getWidth(), Gdx.graphics.getHeight());

		img = new Texture("badlogic.jpg");

		for(FileHandle _file : Gdx.files.internal("tiles/").list()) {
			tiles.add(new Tile(_file.path()));
		}

	}

	@Override
	public void render () {
		cameraZoomFactor -= 0.02f * Gdx.graphics.getDeltaTime();
		camera.update();
		camera.zoom = cameraZoomFactor;
		ScreenUtils.clear(1, 0, 0, 1);
		batch.setProjectionMatrix(camera.combined);
		batch.begin();
		batch.draw(img, 0, 0);

		int row = 0;
		int column = 0;
        for (Number number : tilemap) {
            if (column >= tilemapBreak) {
                row++;
                column = 0;
            }
            int tileId = (int) number;

            tiles.get(tileId).draw(batch, column * 16, row * 16);

            column++;
        }

		batch.end();
	}
	
	@Override
	public void dispose () {
		batch.dispose();
		img.dispose();
	}
